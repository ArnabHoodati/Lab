class Floating {
    public static void main(String[] args)
    {
  
        // f is to ensures that numbers are float data
        float f1 = 10.5f;
        float f2 = 20.6f;
  
        // to store the multiplied value
        float f = f1 * f2;
  
        // to print the answer
        System.out.println("The answer is: " + f);
}
}